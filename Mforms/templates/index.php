	</center>
	<h4>Породистыми считаются кошки, обладающие определёнными чертами или свойствами, присущими одной из зарегистрированных пород, 
	которая признаётся одной из фелинологических организаций. Различные фелинологические организации признают разное количество пород кошек. 
	Каждая фелинологическая организация имеет своё представление о признанной ей породе, что закреплено в специальном описании данной породы, 
	которое называется стандартом породы. Как правило, у породистой кошки имеется родословная, выданная фелинологическим клубом, 
	которая подтверждает принадлежность данного животного к какой-то определённой породе и его соответствие стандарту указанной в родословной породе. 
	Среди общего числа кошек менее одного процента животных являются породистыми и могут участвовать в выставках кошек — показах-соревнованиях, 
	по выставочным классам, предусмотренным для породистых кошек. Беспородные кошки также могут участвовать в выставках, но по классу домашних любимцев. 
	Запрещаются вязки кошек одной породы с кошками другой породы (ауткроссы), за исключением тех пород, процесс образования которых ещё не завершён.</h4>



	
