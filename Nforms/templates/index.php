	<p>
	Work and Travel USA (рус. Работа и Путешествие в США) — самая популярная программа международного студенческого обмена 
	(Ежегодная квота Госдепартамента на участников этой программы свыше 100 000 мест), 
	целью которой является предоставление студентам дневной формы обучения высших учебных заведений и средне-специальных учебных заведений 
	(с сезона 2010 года) возможности непосредственного участия в повседневной жизни народа Соединенных Штатов Америки через путешествия и временную работу
	на срок до пяти месяцев во время летних каникул (из них до 4 месяцев можно работать, и до 30 дней — путешествовать, так называемый «grace period»).
	</p>
