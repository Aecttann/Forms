	<h3>Условия</h3>
	</center>
	<p>
	Согласно требованиям Госдепартамента для студентов из России, программа может начинаться не ранее 15 мая и закончиться - не позднее 15 сентября.

	Для участия в летней программе Work and Travel USA студентом считается учащийся, 
	числящийся в аккредитованном учебном заведении и добросовестно посещающий занятия, 
	а также владеющий английским языком на достаточном уровне(Intermediate Level). Формальных ограничений по возрасту участников не существует, однако, 
	студенты, которые старше «типичного» студента в России — то есть старше 23-24 лет попадают в группу риска при прохождении собеседования в Посольстве США.

	Государственный департамент США руководит программой обмена в соответствии с постановлением о взаимном образовании и 
	Законом о культурном обмене от 1961 года с внесенными в него поправками. Государственный департамент США определяет организации для управления программами
	международного обмена в нескольких категориях. Эти организации называются спонсорами. У большинства спонсоров есть партнеры и агенты по всему миру.	
